package cn.wmyskxz.entity;

public class ClassInfo {
	
	private int cno;
	private int snum;
	private String head;
	
	public String getHead() {
		return head;
	}
	public void setHead(String head) {
		this.head = head;
	}
	public int getcno() {
		return cno;
	}
	public void setcno(int cno) {
		this.cno = cno;
	}
	public int getsnum() {
		return snum;
	}
	public void setsnum(int snum) {
		this.snum = snum;
	}

}
